public class TestSwitch {
    public static void main(String[] args) {
        int ch = 2;
        switch (ch){
            case 1:
                System.out.println("内容是1");
            case 2:
                System.out.println("内容是2");
            case 3:
                System.out.println("内容是3");
                default:
                    System.out.println("默认值");
        }
    }
}    
    